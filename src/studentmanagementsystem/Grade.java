private void btnSaveGradeActionPerformed(java.awt.event.ActionEvent evt) {
    // 1. التحقق من أن الحقل ليس فارغاً
    String gradeValue = txtGrade.getText().trim();
    if (gradeValue.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "خطأ: الرجاء إدخال درجة الطالب!");
        return; // توقف هنا ولا تكمل
    }

  try {
    // 1. استعلام لإدخال البيانات
    // ملاحظة: استبدلنا student_id بـ student_id مباشرة، سنحتاج لتحويل الاسم لرقم
    String query = "INSERT INTO grades (student_id, subject_name, grade_value) VALUES ((SELECT student_id FROM students WHERE name = ?), ?, ?)";
    
    java.sql.PreparedStatement pst = DatabaseConnection.getConnection().prepareStatement(query);
    
    // 2. ربط القيم (تأكدي من ترتيب القوائم المنسدلة في الواجهة)
    pst.setString(1, jComboBox1.getSelectedItem().toString()); // اسم الطالب (سيتم تحويله لرقم عبر الاستعلام الفرعي)
    pst.setString(2, jComboBox2.getSelectedItem().toString()); // اسم المادة (موجود في subject_name)
    pst.setString(3, txtGrade.getText()); // القيمة
    
    pst.executeUpdate();
    javax.swing.JOptionPane.showMessageDialog(this, "تم حفظ الدرجة بنجاح");
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "خطأ في الحفظ: " + e.getMessage());
}
}

