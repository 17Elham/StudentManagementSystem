    // دالة واحدة تقوم بتعبئة أي قائمة (ComboBox)
public void loadComboBox(javax.swing.JComboBox<String> comboBox, String sql, String columnName) {
    try {
        java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/studentmanagementdb", "root", "Elham");
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery(sql);
        
        comboBox.removeAllItems();
        while (rs.next()) {
            comboBox.addItem(rs.getString(columnName));
        }
        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "خطأ: " + e.getMessage());
    }
}
    
